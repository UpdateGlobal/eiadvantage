<div class="divider"></div>
			<h5>Conciencia de sí mismo</h5>
			<p class="lead"> Consciencia de Sí Mismo se basa en darse cuenta de sus propios sentimientos y del impacto que ellos pueden tener sus decisiones, comportamiento y desempeño. La gente que es emocionalmente consiente de sí misma percibe el rol que pueden jugar sus sentimientos en estas áreas y está mejor preparada para manejar bien esta influencia. Cuando somos emocionalmente conscientes de nosotros mismos, estamos <b>presentes</b> con el rol que están jugando los sentimientos en nuestras decisiones, comportamiento y desempeño. Cuando no, a menudo estamos <b>desconectados</b> de esta influencia.</p>

			<ul style="padding-top: 70px;">
				<li>La conducta de un líder puede impactar positiva o negativamente en el desempeño y compromiso de sus colegas,  </li>
				<li>Los líderes necesitan conocer sus fortalezas y limitaciones a fin de mejorar continua- mente y sostener el éxito, y</li>
				<li>Los líderes interpretan los hechos en el trabajo haciendo uso de su inteligencia, personalidad, valores y creencias, pero también con las limitaciones que esos mismos factores les imponen. A fin de evaluar objetivamente los hechos, los líderes deben saber cómo ellos interpretan al mundo y cómo esto los ayuda o los limita.</li>
			</ul>

<div class="divider"></div>
			<h5>Consciencia de los Demás</h5>
			<p class="lead">Consciencia de los Demás radica en percibir y reconocer a los demás, asegurarse de que se sientan valorados y en adecuar su estilo de liderazgo para que encaje mejor con los otros. Los líderes con un alto nivel en esta habilidad se les describe a menudo como <b>Empáticos</b>, en vez de <b>Insensibles</b>, con los demás y sus sentimientos. La Consciencia de los Demás es importante en el liderazgo debido a que:</p>
			<ul style="padding-top: 70px;">
				<li>El liderazgo consiste fundamentalmente en promover el desempeño y cómo se sienten los demás tiene una relación directa con la forma como se desempeñan,</li>
				<li>La Consciencia de los Demás es necesaria a fin de dar pasos efectivos para influir y promover el desempeño de los demás.</li>
				<li>Para sacar lo mejor de la gente, los líderes necesitan adecuar su estilo de liderazgo de manera que encaje mejor con ella y la situación que están liderando.</li>
			</ul>



<div class="divider"></div>
			<h5>Autenticidad
			</h5>
			<p class="lead">Autenticidad consiste en expresarse abiertamente y de manera efectiva, honrando compromisos y promoviendo esta conducta en los demás. Implica expresar apropiadamente sentimientos específicos en el trabajo, tales como felicidad y frustración, dar feedback a sus colegas acerca de cómo se siente usted y expresar emociones en el momento correcto, en el grado correcto y a la gente correcta. A los líderes con un alto nivel en autenticidad se les describe con frecuencia como <b>Genuinos</b>, mientras que aquellos que muestran un bajo nivel se les califica a menudo como <b>No Confiables.</b> La Autenticidad es importante en el liderazgo debido a que:  </p>
			<ul style="padding-top: 70px;">
				<li>Ayuda a los líderes a crear entendimiento, apertura y sentimientos de confianza en los demás,</li>
				<li>Los líderes que son cautelosos, evitan los conflictos o, por el contrario, son torpemente toscos con la forma de sentir de los demás, pueden generar desconfianza, armonía fingida e incomprensión con aquellos que lo(a) rodean</li>
				<li>Los líderes necesitan que su gente sea abierta con ellos. Si como líder, usted no da el ejemplo mostrando esta conducta, sus reportes directos desconfiarán de usted.</li>
			</ul>



<div class="divider"></div>

			<h5>Razonamiento Emocional
			</h5>
			<p class="lead">Razonamiento Emocional se refiere a la habilidad de usar la información emocional (de usted y de los demás), combinándola con otros hechos y datos, al tomar decisiones. Los líderes con un alto nivel en esta habilidad toman decisiones Expansivas, mientras que aquellos que muestran un bajo nivel toman a menudo decisiones más Limitadas, basadas sólo en hechos y datos técnicos. El Razonamiento Emocional es importante en el liderazgo debido a que:
			</p>
			<ul style="padding-top: 70px;">
				<li>Los sentimientos y emociones contienen información importante. Por ejemplo, si un colega está mostrando frustración o estrés, estos sentimientos nos dicen que va a ser menos abierto y receptivo a nuevas ideas e información.
			</li>
				<li>El entorno de trabajo se está volviendo cada vez más complejo y acelerado. Esto exige tomar rápidamente mejores decisiones, sin tener todos los hechos y datos técnico. En estas circunstancias, son importantes la intuición y las corazonadas, y</li>
				<li>Las emociones influyen en las personas. Si usted no toma en cuenta cómo probable- mente se sentirá y reaccionará la gente ante la toma de decisiones, es posible que no logre el apoyo o convencimiento necesarios para sus decisiones.</li>
			</ul>




<div class="divider"></div>

			<h5>Autocontrol</h5>
			<p class="lead">Autocontrol consiste en manejar sus propios estados de ánimo y emociones, tiempo y conducta, así como en tratar siempre de mejorar. Esta competencia de liderazgo emocionalmente inteligente es particularmente importante. A los líderes con un alto nivel en autocontrol se les describe a menudo como <b>Resilientes</b>, en vez de <b>Inconsistentes</b>, en el trabajo. El mundo moderno del trabajo impone altas exigencias y estrés, que pueden provocar consecuencias y emociones negativas. El Autocontrol es importante en el liderazgo debido a que:</p>
			<ul style="padding-top: 70px;">
				<li>El estado de ánimo de un líder puede ser muy contagioso y, por consiguiente, constituir una fuerza poderosa en el trabajo, tanto productiva como improductivamente.</li>
				<li>Esta habilidad ayuda a los líderes a ser resilientes y capaces de manejar adecuadamente altas exigencias y estrés en el trabajo, y </li>
				<li>Para alcanzar, mantener y mejorar el éxito, los líderes necesitan prestar atención consciente a la forma de manejar el tiempo, a su manera de comportarse y mejorar continuamente cómo lideran a los demás.</li>
			</ul style="padding-top: 70px;">
<div class="divider"></div>
			<h5>Inspirar Resultados</h5>
			<p class="lead">Inspirar Resultados es propiciar un alto desempeño en los demás, a través de la solución de problemas, promoviendo, reconociendo y apoyando el trabajo de la gente. El desempeño de una persona puede controlarse con indicadores claves de desempeño (KPIs); sin embargo, los estudios han demostrado que este estilo de "cumplimiento" no logra a menudo impulsar el esfuerzo discrecional/voluntario y el alto desempeño. Los líderes que adoptan un estilo más inspirador, frecuentemente Potencian a los demás para rendir por encima y más allá de lo que se espera de ellos. Inspirar Resultados es importante en el liderazgo debido a que:</p>
			<ul>
				<li>El liderazgo consiste fundamentalmente en promover el alto desempeño de la gente.</li>
				<li>Gestionar el rendimiento de la gente con reglas e indicadores clave de desempeño (KPIs) normalmente produce un resultado "esperado", en vez de un resultado "inesperadamente" alto.</li>
				<li>A menudo, la gente aprende y se desarrolla más con este tipo de estilo de liderazgo, generando mejoras continuas de desempeño, año tras año.</li>
			</ul>



