			<p>La diferencia entre estos dos individuos es el grado de habilidades de inteligencia emocional que poseen y demuestran. Como acaba de experimentar, estas habilidades son fundamentales en el lugar de trabajo.<br><br>

			<b>La inteligencia emocional</b>	 comprende una serie de habilidades diferentes que nos ayudan a reconocer y reconocer las emociones de los demás y dentro de nosotros mismos. El uso de habilidades de Inteligencia Emocional es tan importante como el uso de habilidades intelectuales (CI) para lograr logros en su vida laboral y personal. Al considerar el mundo del trabajo, la investigación demuestra que la Inteligencia Emocional de un Líder es el principal factor que impulsa la participación de los empleados.<br><br>

			Independientemente de su estado o posición en la vida o el trabajo, cada persona en este planeta interactúa con los demás de alguna manera. Cuando somos conscientes de nuestras propias emociones y comprendemos cómo afectan nuestro comportamiento diario, hemos abierto la puerta al éxito y al logro tanto en nuestro entorno de trabajo como en nuestra vida hogareña.<br><br>

		Estadisticss de apoyo:</p>


		<ul style="padding-left: 70px;">
				<li>Después de 20 años de investigación y 60,000 entrevistas de salida, el Instituto Saratoga informa que el 80% de la facturación está directamente relacionada con relaciones insatisfactorias con el jefe</li>
				<li>Según un estudio de la Organización Gallup de aproximadamente 1 millón de trabajadores, la razón principal por la que las personas dejan sus trabajos es por los "malos jefes".</li>
				<li>Una conclusión a la que llega Roger Herman es que ¾ de las personas que voluntariamente abandonan sus trabajos no abandonan sus trabajos; ellos renunciaron a sus jefes. (Herman, Roger. Ganar la guerra por el talento. El ASTD Trends Watch: las fuerzas que dan forma al rendimiento y la mejora del lugar de trabajo).</li>
				<li>Otro estudio reciente de Gallup encontró que los grupos de trabajo pobremente administrados tienen un promedio de 50 por ciento menos productivo y 44 por ciento menos rentable que los grupos bien administrados.</li>
				<li>Ser víctima de un jefe brutal conduce a una depresión clínica en el 41 por ciento de las víctimas, según una encuesta de Bullybusters.org, y una organización sin fines de lucro en línea en Benicia, California, que asesora a las víctimas de abuso en el lugar de trabajo.</li>
