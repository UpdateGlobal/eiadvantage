			<h5>El vínculo entre la inteligencia emocional y el compromiso de los empleados</h5>

			<p>La investigación muestra que el vínculo entre la Inteligencia Emocional y la Participación de los Empleados es profundo. Un estudio de 2010 de más de 400,000 empleados que se nutrieron de sus 40,000 líderes-gerentes mostró que los siguientes comportamientos provocaron un compromiso en ellos:<br><br>

La capacidad de escuchar, crear un ambiente de trabajo positivo, una energía y entusiasmo contagiosos, la preparación para reconocer las contribuciones positivas y su creencia inquebrantable de que cada uno de los miembros de su equipo tiene algo especial que ofrecer.<br><br>

En resumen, estos 400,000 empleados identificaron los comportamientos típicos de los líderes Emocionalmente Inteligentes.<br><br>

En 2012, Genos International evaluó a 100 líderes de IBM de EE. UU., Europa y Asia Pacífico por su inteligencia emocional y, al mismo tiempo, se midió el compromiso de los 438 empleados que informaron a este grupo de liderazgo.<br><br>

En el gráfico a continuación, cada punto representa el puntaje de compromiso de un empleado. El eje x del gráfico representa la inteligencia emocional de los líderes como un puntaje percentil: los puntajes entre 1 y 39 son bajos, 40-59 son promedio y más de 60 son altos.<br><br></p>


