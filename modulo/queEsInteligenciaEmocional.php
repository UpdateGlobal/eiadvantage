			<h1>Qué es <span>Inteligencia </span>Emocional</h1>
			<br>
			<p align="center ">La inteligencia emocional implica un conjunto de habilidades que definen qué tan bien reconocemos y respondemos a las emociones. Se ha demostrado que estas habilidades son la base de muchos aspectos del éxito en la vida, razón por la cual la inteligencia emocional ha redefinido lo que significa ser inteligente.</p>
			 <br><br>

		</div>
		<div class="col-sm-6" align="right">
			<p>Cuando hablamos sobre las emociones, debemos reconocer que pueden afectar nuestro juicio, nuestras acciones y nuestros logros, tanto de manera positiva como negativa. Intente recordar un momento en que se encontró con una emoción, digamos alegría o felicidad. Esta emoción probablemente se experimentó porque usted logró algo positivo en su vida, como el tiempo que pasó con amigos o familiares o un logro en el lugar de trabajo. Pregúntese cómo se siente esta emoción positiva; ¿impactó en su humor, su resistencia o la forma en que interactuó con otras personas a su alrededor? Ahora, visualice un recuerdo de una emoción menos positiva, como animosidad o exasperación. Esta emoción puede haber surgido por una serie de razones, tal vez un colega de trabajo dijo algo con lo que no estaba de acuerdo o un amigo cercano le decepcionó. Hágase la misma pregunta, ¿Cómo afectó esta emoción tus acciones? ¿Actuaste a toda prisa o dijiste algo que luego lamentarías?

			Para abarcar la Inteligencia Emocional (EI) tenemos que entender que comprende una serie de habilidades diferentes que nos ayudan a reconocer y reconocer las emociones de los demás y de hecho dentro de nosotros mismos. El uso de habilidades de Inteligencia Emocional es tan importante como el uso de habilidades intelectuales (CI) para lograr logros en su vida laboral y personal.

			Independientemente de su estado o posición en la vida o el trabajo, cada persona en este planeta interactúa con los demás de alguna manera. Cuando somos conscientes de nuestras propias emociones y comprendemos cómo afectan nuestro comportamiento diario, hemos abierto la puerta al éxito y al logro tanto en nuestro entorno de trabajo como en nuestra vida hogareña.</p>
